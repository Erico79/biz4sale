        //
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Category::destroy($id);

        request()->session()->flash('success', 'Category has been deleted');
        return redirect('categories');
    }
}
